function play(){
    
    if(playStatus===false){
        audio.play()
        .then(()=>{
            playStatus=true;
            moving();
        })
    }
    else{
        audio.pause()
        playStatus=false;
        clearInterval(timeline);
    }
}

//close modal
function closeModal(){
    ele=document.getElementsByClassName('modal');
    ele[0].style="z-index:-1"
    ele[0].style="display:none"
    modalStatus=false;
    
}

